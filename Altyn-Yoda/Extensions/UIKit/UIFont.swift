import UIKit.UIFont

extension UIFont {
    
    class func interBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-Bold", size: size)!
    }
    
    class func interMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-Medium", size: size)!
    }
    
    class func interRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-Regular", size: size)!
    }
    
    class func interSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-SemiBold", size: size)!
    }
    
    class func interMediumItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-MediumItalic", size: size)!
    }
    
    class func interItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-Italic", size: size)!
    }
    
    class func interThin(size: CGFloat) -> UIFont {
        return UIFont(name: "Inter-Thin", size: size)!
    }
    
}
